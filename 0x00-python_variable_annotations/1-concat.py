#!/usr/bin/env python3
"""
type-annotated function concat that
takes a string str1 and a string str2
as arguments and returns a
concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Args:
         str1- string to concatenate
         str2- string to concatenate
    Returns:
        Concatenated string
    """
    return str1+str2
